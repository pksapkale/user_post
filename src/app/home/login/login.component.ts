import { Component, inject } from '@angular/core';
import { FormsModule } from '@angular/forms';
import { AuthHelperServiceService } from '../services/auth-helper-service.service';
import { Router } from '@angular/router';

@Component({
  selector: 'app-login',
  standalone: true,
  imports: [FormsModule],
  templateUrl: './login.component.html',
  styleUrl: './login.component.css',
})
export class LoginComponent {
  email_pattern =
    /(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/;

  authHelperService: AuthHelperServiceService = inject(AuthHelperServiceService);
  _router: Router = inject(Router);

  loginSubmitHandler(loginForm: any) {
    if(loginForm.status == 'VALID') {
      let obj = loginForm.value;
      this.authHelperService.logInUser(obj);
    }
    else {
      alert('Form is invalid');
    }
  }

  jumpToPage(route: string) {
    this._router.navigateByUrl(route);
  }
}
